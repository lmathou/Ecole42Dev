int		ft_strcmp(char *s1, char *s2)
{
	int dif;

	dif = 0;
	while (s1[dif] == s2[dif] && s1[dif] != '\0' && s2[dif] != '\0')
		dif++;
	return (s1[dif] - s2[dif]);
}
