char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int		k;

	k = 1;
	while ((*src != '\0') && (k <= n))
	{
		*dest = *src;
		dest++;
		src++;
		k++;
	}
	*dest = '\0';
	return (dest);
}
